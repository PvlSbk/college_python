# 7) Найдите трехзначные числа, равные сумме кубов своих цифр.
for i in range(100, 1000):
    if i == ((i // 100) ** 3 + ((i // 10) % 10) ** 3 + (i % 10) ** 3):
        print(i)
# 8) Сколько существует четырехзначных чисел, которые в 600 раз больше суммы своих цифр?
