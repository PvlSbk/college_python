# 5) Вывести все пятизначные числа, которые делятся на 2,
# у которых средняя цифра нечетная, и сумма всех цифр делится на 4
for i in range(10000, 100000):
    if i % 2 == 0:
        digit = (i // 100) % 10
        sum = 0
        tempNumber = i
        while tempNumber > 0:
            sum += tempNumber % 10
            tempNumber //= 10
        if digit % 2 != 0 and sum % 4 == 0:
            print(i)
# 6) Вывести на экран числа от 1000 до 9999 такие, что среди цифр есть цифра 3.
